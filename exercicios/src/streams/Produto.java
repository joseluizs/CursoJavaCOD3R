package streams;

public class Produto {

	final String nome;
	final Double preco; 
	final Double desconto;
	final Double valorFrete;

	public Produto(String nome, Double preco, Double desconto, Double valorFrete) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.valorFrete = valorFrete;
	}
	
	
}
